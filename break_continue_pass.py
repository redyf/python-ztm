# Break
# É usado para parar um loop se necessário

# Continue
# Ao contrário do break, ele diz para continuar o loop ou seja voltar para o ínicio. Se tiver uma condição logo após o continue, ela nunca será executada pois o continue fará com que volte para o início do loop.

# Pass
# Não é muito útil porque ele basicamente não faz nada. Ele só passa para a próxima linha.
